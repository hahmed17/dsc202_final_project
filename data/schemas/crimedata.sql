CREATE TABLE IF NOT EXISTS CrimeData (
    ID INT PRIMARY KEY,
    CaseNumber VARCHAR(20),
    Date TIMESTAMP,
    Block VARCHAR(50),
    IUCR TEXT,
    PrimaryType TEXT,
    Description TEXT,
    LocationDescription VARCHAR(100),
    Arrest BOOLEAN,
    Domestic BOOLEAN,
    Beat INT,
    District INT,
    Ward INT,
    CommunityArea INT,
    FBICode VARCHAR(5),
    XCoordinate INT,
    YCoordinate INT,
    Year INT,
    UpdatedOn TIMESTAMP,
    Latitude FLOAT,
    Longitude FLOAT,
    Location TEXT
);
